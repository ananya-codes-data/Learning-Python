# Lists and Tuples

# List
# containers to store a set of values of any data type
# unlike strings, lists are mutable i.e., it can be changed

friends = ["Apple", "Orange", 5, 345.06, False, "Aakash", "Rohan"]
print(friends[0])

friends[0] = "Grapes"

print(friends[0])

# lists can be indexed like string
print(friends[1:4])